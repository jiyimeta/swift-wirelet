@Test func emitModelsConfigDecodesFromJSON() throws {
    let json = #"""
    {
      "defaultModelPackage": "io.example.model",
      "defaultCodecPackage": "io.example.serialization",
      "emitModels": true
    }
    """#
    let config = try JSONDecoder().decode(KotlinCodegenConfig.self, from: Data(json.utf8))
    #expect(config.emitModels == true)
}

@Test func emitModelsDefaultsToFalseWhenAbsentFromJSON() throws {
    let json = #"""
    {
      "defaultModelPackage": "io.example.model",
      "defaultCodecPackage": "io.example.serialization"
    }
    """#
    let config = try JSONDecoder().decode(KotlinCodegenConfig.self, from: Data(json.utf8))
    #expect(config.emitModels == false)
}
