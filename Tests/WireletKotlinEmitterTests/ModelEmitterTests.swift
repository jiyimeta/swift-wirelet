import Foundation
import Testing
@testable import WireletKotlinEmitter
import WireletSchema

@Test func doesNotEmitModelsByDefault() throws {
    let schema = Schema(types: [
        .struct(WireStruct(
            name: "Point",
            fields: [
                WireField(name: "x", typeText: "Int32", tag: 1),
                WireField(name: "y", typeText: "Int32", tag: 2),
            ],
            kotlinTarget: .auto,
        )),
    ])
    let config = KotlinCodegenConfig(
        defaultModelPackage: "io.example.model",
        defaultCodecPackage: "io.example.serialization",
    )

    let files = try KotlinEmitter(config: config).emit(schema: schema)

    #expect(files.count == 1)
    #expect(files[0].relativePath == "io/example/serialization/PointCodec.kt")
}

