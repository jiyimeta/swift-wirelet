@Test func structModelImportsCrossPackageReferences() throws {
    let schema = Schema(types: [
        .struct(WireStruct(
            name: "Outer",
            fields: [
                WireField(name: "inner", typeText: "Inner", tag: 1),
            ],
            kotlinTarget: .auto,
        )),
        .struct(WireStruct(
            name: "Inner",
            fields: [
                WireField(name: "v", typeText: "Int32", tag: 1),
            ],
            kotlinTarget: .auto,
        )),
    ])
    let config = KotlinCodegenConfig(
        defaultModelPackage: "io.example.model",
        defaultCodecPackage: "io.example.serialization",
        rules: [
            Rule(pattern: "Inner", modelPackage: "io.example.other"),
        ],
        emitModels: true,
    )

    let files = try KotlinEmitter(config: config).emit(schema: schema)
    let outer = try #require(files.first { $0.relativePath == "io/example/model/Outer.kt" })
    #expect(outer.content.contains("import io.example.other.Inner"))
    let inner = try #require(files.first { $0.relativePath == "io/example/other/Inner.kt" })
    #expect(!inner.content.contains("import"))
}

@Test func structModelOmitsSamePackageImports() throws {
    let schema = Schema(types: [
        .struct(WireStruct(
            name: "Outer",
            fields: [
                WireField(name: "items", typeText: "[Inner]", tag: 1),
            ],
            kotlinTarget: .auto,
        )),
        .struct(WireStruct(
            name: "Inner",
            fields: [
                WireField(name: "v", typeText: "Int32", tag: 1),
            ],
            kotlinTarget: .auto,
        )),
    ])
    let config = KotlinCodegenConfig(
        defaultModelPackage: "io.example.model",
        defaultCodecPackage: "io.example.serialization",
        emitModels: true,
    )

    let files = try KotlinEmitter(config: config).emit(schema: schema)
    let outer = try #require(files.first { $0.relativePath == "io/example/model/Outer.kt" })
    #expect(!outer.content.contains("import io.example.model.Inner"))
    #expect(outer.content.contains("val items: List<Inner>"))
}

